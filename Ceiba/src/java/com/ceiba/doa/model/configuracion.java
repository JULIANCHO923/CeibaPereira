package com.ceiba.doa.model;


public class configuracion {
    
    private double porcentae_inmueble;
    private int cuota;
    private double comision;

    public double getPorcentae_inmueble() {
        return porcentae_inmueble;
    }

    public void setPorcentae_inmueble(double porcentae_inmueble) {
        this.porcentae_inmueble = porcentae_inmueble;
    }

    public int getCuota() {
        return cuota;
    }

    public void setCuota(int cuota) {
        this.cuota = cuota;
    }

    public double getComision() {
        return comision;
    }

    public void setComision(double comision) {
        this.comision = comision;
    }
    
}
